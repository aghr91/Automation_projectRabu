package Rabu2nd;

public class DynamicArray {
    public static void main(String[] args) {

        String[] ZipCode = new String[5];
        ZipCode[0] = "3454";
        ZipCode[1] = "12345";
        ZipCode[2] = "3421";
        ZipCode[3] = "3467";
        ZipCode[4] = "34556";
        System.out.println("my last zip code is " + ZipCode[3]);

        int[] checkAmount = new int[4];
        checkAmount[0] = 10000;
        checkAmount[1] = 20000;
        checkAmount[2] = 30000;
        checkAmount[3] = 40000;
        System.out.println("my last check amount is " + checkAmount[3]);

    }//end of method
}//end of java class
